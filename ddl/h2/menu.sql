    ADD CONSTRAINT PRUNUS_BIZ_MENU_DICTIONARY_UNIQUE_01 UNIQUE (MENU_ID, LANGUAGE);

CREATE SEQUENCE MENU_PROGRAM_SEQ START WITH 1 INCREMENT BY 1;
CREATE TABLE PRUNUS_BIZ_MENU_PROGRAM
(
    PROGRAM_ID   BIGINT NOT NULL,
    MENU_ID      BIGINT,
    PROGRAM_TYPE VARCHAR(50),
    URL          VARCHAR(300) NOT NULL,
    ACTIVE       BOOLEAN NOT NULL,
    PRIMARY KEY (PROGRAM_ID)
);

